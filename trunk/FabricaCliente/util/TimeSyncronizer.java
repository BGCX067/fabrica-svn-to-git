package util;

public class TimeSyncronizer {

		private long tiempo;

		public TimeSyncronizer(long tiempo) {
			this.tiempo = tiempo;
		}

		public TimeSyncronizer() {
			super();
		}

		public long getTiempo() {
			return tiempo;
		}

		public void setTiempo(long tiempo) {
			this.tiempo = tiempo;
		}
		
		
}
